    function normalizeLanguage(language) {
        if (!language) {
            return DEFAULT_LANGUAGE;
        }
        const raw = String(language).trim();
        const exact = SUPPORTED_LANGUAGES.find((item) => item.code.toLowerCase() === raw.toLowerCase());
        if (exact) {
            return exact.code;
        }
        const prefix = raw.toLowerCase().split("-")[0];
        const fallback = SUPPORTED_LANGUAGES.find((item) => item.code.toLowerCase().split("-")[0] === prefix);
        return fallback ? fallback.code : DEFAULT_LANGUAGE;
    }

    function detectInitialLanguage() {
        try {
            const stored = window.localStorage.getItem(STORAGE_KEY);
            if (stored) {
                return normalizeLanguage(stored);
            }
        } catch (e) {
            // ignore storage errors
        }
        return normalizeLanguage(window.navigator.language || DEFAULT_LANGUAGE);
    }

    let currentLanguage = detectInitialLanguage();

    function formatTemplate(template, params = {}) {
        return String(template).replace(/\{(\w+)\}/g, (_, key) => {
            const value = params[key];
            return value === undefined || value === null ? "" : String(value);
        });
    }

    function t(key, params = {}) {
        const langMessages = MESSAGES[currentLanguage] || {};
        const defaultMessages = MESSAGES[DEFAULT_LANGUAGE] || {};
        const template = langMessages[key] ?? defaultMessages[key] ?? key;
        return formatTemplate(template, params);
    }

    function applyTranslations(root = document) {
        if (!root || typeof root.querySelectorAll !== "function") {
            return;
        }
        document.documentElement.lang = currentLanguage;
        root.querySelectorAll("[data-i18n]").forEach((el) => {
            el.textContent = t(el.dataset.i18n);
        });
        root.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
            el.setAttribute("placeholder", t(el.dataset.i18nPlaceholder));
        });
        root.querySelectorAll("[data-i18n-title]").forEach((el) => {
            el.setAttribute("title", t(el.dataset.i18nTitle));
        });
        root.querySelectorAll("[data-i18n-aria-label]").forEach((el) => {
            el.setAttribute("aria-label", t(el.dataset.i18nAriaLabel));
        });
        root.querySelectorAll("[data-i18n-value]").forEach((el) => {
            el.setAttribute("value", t(el.dataset.i18nValue));
        });
    }

    function setLanguage(language, options = {}) {
        currentLanguage = normalizeLanguage(language);
        document.documentElement.lang = currentLanguage;
        try {
            window.localStorage.setItem(STORAGE_KEY, currentLanguage);
        } catch (e) {
            // ignore storage errors
        }
        if (options.apply !== false) {
            applyTranslations(document);
        }
        if (options.emit !== false) {
            window.dispatchEvent(new CustomEvent("webssh:languagechange", {
                detail: { language: currentLanguage }
            }));
        }
        return currentLanguage;
    }

    function getLanguage() {
        return currentLanguage;
    }

    function renderRule(rule, payload) {
        if (rule.key) {
            return t(rule.key, payload);
        }
        return "";
    }

    function translateKnownMessage(message) {
        if (message === undefined || message === null || message === "") {
            return "";
        }
        const text = String(message);
        for (const rule of RUNTIME_RULES) {
            if (rule.exact && text === rule.exact) {
                return renderRule(rule, {});
            }
            if (rule.prefix && text.startsWith(rule.prefix)) {
                const rawValue = text.slice(rule.prefix.length);
                const payload = {
                    [rule.param || "value"]: rule.translateParam ? translateKnownMessage(rawValue) : rawValue
                };
                return renderRule(rule, payload);
            }
            if (rule.regex) {
                const match = text.match(rule.regex);
                if (!match) {
                    continue;
                }
                const payload = typeof rule.params === "function" ? rule.params(match) : {};
                return renderRule(rule, payload);
            }
        }
        return text;
    }

    function mountLanguageSelector(selectEl) {
        if (!selectEl) {
            return;
        }
        selectEl.innerHTML = SUPPORTED_LANGUAGES.map((item) => {
            return `<option value="${item.code}">${item.label}</option>`;
        }).join("");

        const syncValue = () => {
            if (selectEl.value !== currentLanguage) {
                selectEl.value = currentLanguage;
            }
        };

        syncValue();
        selectEl.addEventListener("change", () => {
            setLanguage(selectEl.value);
        });
        window.addEventListener("webssh:languagechange", syncValue);
    }

    document.documentElement.lang = currentLanguage;

    window.webSshI18n = {
        SUPPORTED_LANGUAGES,
        applyTranslations,
        getLanguage,
        mountLanguageSelector,
        setLanguage,
        t,
        translateKnownMessage
    };
